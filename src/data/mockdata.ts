export interface Medication {
  id: string;
  name: string;
  generic: string;
  brandNames?: string[];
  drugClass: string;
  uses: string[];
  mechanism: string;
  prescriptionStatus: string;
  admetScore: number;
  dosage: {
    adult: string;
    child?: string;
    elderly?: string;
    maxDaily: string;
    duration?: string;
    timing: string;
    foodInteraction: string;
  };
  safety: {
    sideEffects: string[];
    seriousSideEffects?: string[];
    allergies: string;
    contraindications: string[];
    pregnancyCategory: string;
    breastfeeding: string;
    interactions: string[];
  };
  admet: {
    molecularWeight: string;
    logP: string;
    hba: number;
    hbd: number;
    tpsa: string;
    lipinskiViolations: number;
    qedScore: string;
  };
  expiryInfo: string;
  missedDose: string;
  overdoseSymptoms?: string;
}

